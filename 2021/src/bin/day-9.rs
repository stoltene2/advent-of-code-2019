type Ocean = Vec<Vec<i32>>;

fn main() {
    let data = input();

    let mut sum = 0;
    for y in 0..100 {
        for x in 0..100 {
            sum = sum + minimal_sum(&data, x, y);
        }
    }

    println!("Result: {}", &sum);
}

fn minimal_sum(data: &Ocean, x: i32, y: i32) -> i32 {
    let up = lookup(&data, x, y - 1);
    let down = lookup(&data, x, y + 1);
    let left = lookup(&data, x - 1, y);
    let right = lookup(&data, x + 1, y);
    let v = lookup(&data, x, y);

    if v < up && v < down && v < left && v < right {
        return 1 + v;
    } else {
        return 0;
    }
}

fn lookup(data: &Ocean, x: i32, y: i32) -> i32 {
    if x < 0 || y < 0 || x > 99 || y > 99 {
        return i32::MAX;
    } else {
        return data[y as usize][x as usize];
    }
}

