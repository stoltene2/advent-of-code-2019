use std::cmp::Ordering;
use std::collections::{BinaryHeap, HashMap};
use std::convert::TryInto;

type Point = (usize, usize);

#[derive(Copy, Clone, Eq, PartialEq)]
struct State {
    cost: usize,
    position: Point,
}

// The priority queue depends on `Ord`.
// Explicitly implement the trait so the queue becomes a min-heap
// instead of a max-heap.
impl Ord for State {
    fn cmp(&self, other: &Self) -> Ordering {
        // Notice that the we flip the ordering on costs.
        // In case of a tie we compare positions - this step is necessary
        // to make implementations of `PartialEq` and `Ord` consistent.
        other
            .cost
            .cmp(&self.cost)
            .then_with(|| self.position.cmp(&other.position))
    }
}

// `PartialOrd` needs to be implemented as well.
impl PartialOrd for State {
    fn partial_cmp(&self, other: &Self) -> Option<Ordering> {
        Some(self.cmp(other))
    }
}

#[derive(Clone)]
struct Edge {
    point: Point,
    cost: usize,
}

fn build_graph(input: Vec<Vec<usize>>) -> Vec<Vec<Edge>> {
    let mut result: Vec<Vec<_>> = Vec::new();
    for (y, row) in input.iter().enumerate() {
        result.push(Vec::new());
        for (x, cost) in row.iter().enumerate() {
            result[y].push(Edge {
                point: (x, y),
                cost: *cost,
            });
        }
    }

    result
}

fn neighbors(graph: &Vec<Vec<Edge>>, (a, b): &Point) -> Vec<Edge> {
    let max_y = graph.len() as i32;
    let max_x = graph[0].len() as i32;

    let x: i32 = *a as i32;
    let y: i32 = *b as i32;
    let ps: Vec<(i32, i32)> = vec![(x, y - 1), (x - 1, y), (x + 1, y), (x, y + 1)];

    ps.iter()
        .filter(|(x, y)| x >= &0 && x < &max_x && y >= &0 && y < &max_y)
        .map(|(i, j)| graph[*j as usize][*i as usize].clone())
        .collect()
}

// Dijkstra's shortest path algorithm.

// Start at `start` and use `dist` to track the current shortest distance
// to each node. This implementation isn't memory-efficient as it may leave duplicate
// nodes in the queue. It also uses `usize::MAX` as a sentinel value,
// for a simpler implementation.
fn shortest_path(adj_list: &Vec<Vec<Edge>>, start: Point, goal: Point) -> Option<usize> {
    let mut dist: HashMap<Point, usize> = HashMap::new();

    for x in 0..=goal.0 {
        for y in 0..=goal.1 {
            dist.insert((x, y), usize::MAX);
        }
    }

    let mut heap = BinaryHeap::new();

    *dist.entry(start).or_insert(usize::MAX) = 0;

    heap.push(State {
        cost: 0,
        position: start,
    });

    while let Some(State { cost, position }) = heap.pop() {
        if position == goal {
            return Some(cost);
        }

        if cost > dist[&position] {
            continue;
        }

        for edge in neighbors(adj_list, &position) {
            let next = State {
                cost: cost + edge.cost,
                position: edge.point,
            };

            if next.cost < dist[&next.position] {
                heap.push(next);
                dist.insert(next.position, next.cost);
            }
        }
    }

    None
}

fn main() {
    let adj_list = build_graph(input());
    let shortest = shortest_path(&adj_list, (0, 0), (99, 99));

    println!("shortest: {:?}", &shortest);
    assert_eq!(&741, &shortest.unwrap());

    let pt2_input = generate_pt_2_input(input());
    let adj_list = build_graph(pt2_input);

    let shortest = shortest_path(&adj_list, (0, 0), (499, 499));

    println!("Part2 shortest: {:?}", &shortest);
    assert_eq!(&2976, &shortest.unwrap());
}

fn generate_pt_2_input(input: Vec<Vec<usize>>) -> Vec<Vec<usize>> {
    let mut result: Vec<Vec<usize>> = Vec::new();

    let new_matrix = input.into_iter().map(|r| iterate_vec(r)).collect();

    iterate_rows(new_matrix)
}

fn iterate_vec(v: Vec<usize>) -> Vec<usize> {
    let mut result = Vec::with_capacity(5 * v.len());
    result.append(&mut v.clone());

    let mut next = next_vec(v);

    for _j in 1..=4 {
        let n = next.clone();
        result.append(&mut next);
        next = next_vec(n);
    }

    result
}

fn iterate_rows(v: Vec<Vec<usize>>) -> Vec<Vec<usize>> {
    let mut result = Vec::with_capacity(5 * v.len());
    result.append(&mut v.clone());

    // calculate this for each row

    let mut next = next_matrix(v);

    for _j in 1..=4 {
        let n = next.clone();
        result.append(&mut next);
        next = next_matrix(n);
    }

    result
}

fn next_matrix(v: Vec<Vec<usize>>) -> Vec<Vec<usize>> {
    let m: Vec<Vec<usize>> = v.clone();

    m.into_iter().map(|row| next_vec(row)).collect()
}

fn next_vec(v: Vec<usize>) -> Vec<usize> {
    v.iter().map(|i| if i == &9 { 1 } else { i + 1 }).collect()
}

