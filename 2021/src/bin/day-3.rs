fn oxygen(data: Vec<u32>, bit_len: u8) -> u32 {
    // 4 is bit length one

    let mut d: Vec<u32> = data;

    for pos in (0..bit_len).rev() {
        let bit_mask = 1 << pos;
        let (ones, zeros): (Vec<u32>, Vec<u32>) =
            d.iter().partition(|&data| (data & bit_mask) != 0);

        if ones.len() >= zeros.len() {
            d = ones;
        } else {
            d = zeros
        }

        if d.len() == 1 {
            break;
        }
    }

    d[0]
}

fn co2_scrubber(data: Vec<u32>, bit_len: u8) -> u32 {
    let mut d: Vec<u32> = data;

    for pos in (0..bit_len).rev() {
        let bit_mask = 1 << pos;
        let (ones, zeros): (Vec<u32>, Vec<u32>) =
            d.iter().partition(|&data| (data & bit_mask) != 0);

        if ones.len() >= zeros.len() {
            d = zeros;
        } else {
            d = ones;
        }

        if d.len() == 1 {
            break;
        }
    }

    d[0]
}

fn power_consumption(data: &Vec<u32>) -> u32 {
    let mut gamma: u32 = 0;

    let n: u32 = 12;
    for pos in 0..n {
        let bit: u32 = 1 << pos;

        let mut hits = 0;
        for d in data {
            if (bit & d) != 0 {
                hits += 1;
            }
        }

        if hits > (data.len() / 2) {
            gamma |= bit;
        }
    }

    // not values together
    let epsilon = (!gamma) & 0b111111111111;

    epsilon * gamma
}

